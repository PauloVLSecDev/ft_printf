#include "ft_printf.h"

int	ft_unsi_int(int n)
{
	int	len;

	len = 0;
	if (n < 0)
	{
		n = n + 4294967295;
	}
	if (n > 9)
	{
			len += ft_unsi_int((n / 10) + '0');
	}
	len += putchar((n % 10) + '0');
	return (len);
}
